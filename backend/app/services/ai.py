def extract_report_text(report_type: str, file_name: str) -> str:
    """
    Simulates OCR extraction on medical documents, returning standard
    diagnostic narratives. Falls back gracefully with an apology if
    inputs are missing or malformed.
    """
    try:
        if not report_type or not isinstance(report_type, str):
            raise ValueError("report_type missing or invalid")
        if not file_name or not isinstance(file_name, str):
            raise ValueError("file_name missing or invalid")

        t = report_type.lower()
        if "pathology" in t:
            return (
                f"Pathology Report - Specimen: {file_name}\n"
                "Clinical History: Breast lump, outer upper quadrant.\n"
                "Gross Description: Sectioning reveals a firm, white mass measuring 2.1 x 1.8 cm.\n"
                "Microscopic Description: Sections show infiltrating ductal carcinoma (Grade II).\n"
                "Surgical Margins: Clear of tumor. Lymph nodes: 0/3 negative."
            )
        elif "radiology" in t or "ct" in t or "mri" in t:
            return (
                f"Radiology Scan - CT Thorax / Abdomen: {file_name}\n"
                "Indication: Staging for newly diagnosed lung adenocarcinoma.\n"
                "Findings: A 3.2 cm lobulated mass is seen in the right lower lobe.\n"
                "No hilar lymphadenopathy. Adrenals: normal size. Bone windows: normal.\n"
                "Impression: Primary lung neoplasm, no evidence of distant metastasis."
            )
        else:
            return (
                f"Laboratory Clinical Panel: {file_name}\n"
                "Complete Blood Count (CBC) and Metabolic Panel.\n"
                "White Blood Cells (WBC): 6.8 K/uL (Normal)\n"
                "Hemoglobin (Hgb): 13.2 g/dL (Normal)\n"
                "Platelets: 240 K/uL (Normal)\n"
                "Liver function enzymes within standard limits."
            )
    except Exception as exc:
        return (
            f"{APOLOGY_EN}(Technical detail: {exc})\n"
            f"{APOLOGY_HI}(तकनीकी विवरण: {exc})"
        )


def generate_report_summary(text: str, lang: str = "en") -> str:
    """
    Extracts staging parameters and primary sites to compile a concise,
    bilingual-labeled summary.
    """
    try:
        if not text or not isinstance(text, str):
            raise ValueError("no report text supplied")

        lines = text.split("\n")
        report_title = lines[0] if lines else "Medical Report Summary"

        primary_site = "Unknown Site"
        histology = "Unknown Histology"
        low = text.lower()
        if "breast" in low:
            primary_site = "Breast"
        elif "lung" in low or "thorax" in low:
            primary_site = "Lung"

        if "ductal" in low:
            histology = "Infiltrating Ductal Carcinoma"
        elif "adenocarcinoma" in low:
            histology = "Adenocarcinoma"

        if lang == "hi":
            summary = (
                f"**दस्तावेज़**: {report_title}\n"
                f"**प्राथमिक स्थान (Primary Site)**: {primary_site}\n"
                f"**हिस्टोलॉजी**: {histology}\n"
                f"**क्लिनिकल स्थिति**: सर्जिकल मार्जिन स्पष्ट। स्थानीयकृत गांठ, कोई मेटास्टेटिक विस्तार नहीं पाया गया।"
            )
        else:
            summary = (
                f"**Document**: {report_title}\n"
                f"**Primary Site**: {primary_site}\n"
                f"**Histology**: {histology}\n"
                f"**Clinical Status**: Specimen clear of surgical margins. Localized mass, no metastatic expansion noted."
            )
        return summary + disclaimer(lang)
    except Exception as exc:
        return apology(lang) + f"(detail / विवरण: {exc})" + disclaimer(lang)


