def _safe_get(d: dict, key: str, default):
    try:
        val = d.get(key, default)
        return val if val not in (None, "") else default
    except Exception:
        return default


def generate_chat_response(message: str, patient_context: dict, lang: str = None) -> str:
    """
    RAG-simulated clinical chat answering patient-specific questions in
    English or Hindi. Language is auto-detected from `message` unless
    explicitly passed via `lang` ('en' or 'hi').
    """
    try:
        if lang not in ("en", "hi"):
            lang = detect_language(message)

        if not isinstance(patient_context, dict):
            raise ValueError("patient_context missing or malformed")

        msg = (message or "").lower()

        first_name = _safe_get(patient_context, "first_name", "the patient" if lang == "en" else "रोगी")
        last_name = _safe_get(patient_context, "last_name", "")
        full_name = f"{first_name} {last_name}".strip()
        gender = _safe_get(patient_context, "gender", "F")
        birth_date = _safe_get(patient_context, "birth_date", "N/A")
        status = _safe_get(patient_context, "status", "active")

        treatments = _safe_get(patient_context, "treatments", [])
        diagnoses = _safe_get(patient_context, "diagnoses", [])
        allergies = _safe_get(patient_context, "allergies", [])
        appointments = _safe_get(patient_context, "appointments", [])
        vitals = _safe_get(patient_context, "vitals", {})

        # --- Greeting -------------------------------------------------
        if any(g in msg for g in ["hello", "hi ", "hi", "namaste", "namaskar"]):
            if lang == "hi":
                return (
                    f"नमस्ते। मैं ऑन्कोलॉजी क्लिनिकल असिस्टेंट हूँ। मैंने {full_name} का चार्ट लोड कर लिया है। "
                    "मैं इलाज की जानकारी, क्लिनिकल टाइमलाइन, या रिपोर्ट सारांश में आपकी कैसे मदद कर सकता हूँ?"
                ) + disclaimer(lang)
            return (
                f"Hello. I am the Oncology Clinical Assistant. I have loaded the chart for {full_name}. "
                "How can I assist you with their treatment history, clinical timelines, or report summaries today?"
            ) + disclaimer(lang)

        # --- Treatments -------------------------------------------------
        if any(k in msg for k in ["treatment", "therapy", "ilaj", "dawai", "chikitsa"]):
            if not treatments:
                if lang == "hi":
                    return f"मुझे {full_name} के चार्ट में कोई सक्रिय या नियोजित इलाज योजना नहीं मिली।" + disclaimer(lang)
                return f"I see no active or planned treatment regimens logged in the chart for {full_name}." + disclaimer(lang)

            tx_list = []
            for t in treatments:
                if not isinstance(t, dict):
                    continue
                ttype = _safe_get(t, "type", "Unknown")
                tstart = _safe_get(t, "start_date", "N/A")
                tstatus = _safe_get(t, "status", "scheduled")
                tx_list.append(f"- {str(ttype).upper()} started on {tstart} ({tstatus})")
            tx_str = "\n".join(tx_list) if tx_list else "-"

            if lang == "hi":
                return (
                    f"{full_name} ({gender}, जन्म तिथि: {birth_date}) के लिए दर्ज उपचार योजनाएँ:\n\n"
                    f"{tx_str}\n\n"
                    "कृपया ध्यान दें: रेडियोथेरेपी योजनाएँ केवल शेड्यूलिंग दिखाती हैं। कोई सक्रिय रेडिएशन डोज़ ट्रैकिंग नहीं की जाती।"
                ) + disclaimer(lang)
            return (
                f"Here are the documented treatment plans for {full_name} ({gender}, DOB: {birth_date}):\n\n"
                f"{tx_str}\n\n"
                "Please note that radiotherapy plans only display scheduling modalities. No active radiation dose tracking is performed."
            ) + disclaimer(lang)

        # --- Diagnosis / staging -----------------------------------------
        if any(k in msg for k in ["diagnosis", "stage", "cancer", "bimari", "rog", "stej"]):
            if not diagnoses:
                if lang == "hi":
                    return f"मुझे {full_name} के लिए कोई स्टेजिंग रिकॉर्ड या प्राथमिक निदान नहीं मिला।" + disclaimer(lang)
                return f"I found no staging records or primary diagnoses logged for {full_name}." + disclaimer(lang)

            diag_list = []
            for d in diagnoses:
                if not isinstance(d, dict):
                    continue
                site = _safe_get(d, "primary_site", "N/A")
                hist = _safe_get(d, "histology", "N/A")
                ddate = _safe_get(d, "diagnosis_date", "N/A")
                diag_list.append(f"- {site} ({hist}) — diagnosed {ddate}")
            diag_str = "\n".join(diag_list) if diag_list else "-"

            if lang == "hi":
                return f"{full_name} के लिए दर्ज निदान:\n\n{diag_str}" + disclaimer(lang)
            return f"Documented diagnoses for {full_name}:\n\n{diag_str}" + disclaimer(lang)

        # --- Allergies -----------------------------------------------------
        if any(k in msg for k in ["allergy", "allergies", "elrji", "alarjee"]):
            if not allergies:
                if lang == "hi":
                    return f"{full_name} के चार्ट में कोई ज्ञात एलर्जी दर्ज नहीं है।" + disclaimer(lang)
                return f"No known allergies are logged for {full_name}." + disclaimer(lang)
            al_str = ", ".join(str(a) for a in allergies)
            if lang == "hi":
                return f"{full_name} की ज्ञात एलर्जी: {al_str}" + disclaimer(lang)
            return f"Known allergies for {full_name}: {al_str}" + disclaimer(lang)

        # --- Appointments ----------------------------------------------------
        if any(k in msg for k in ["appointment", "visit", "milne", "apwaintment", "checkup"]):
            if not appointments:
                if lang == "hi":
                    return f"{full_name} के लिए कोई आगामी अपॉइंटमेंट दर्ज नहीं है।" + disclaimer(lang)
                return f"No upcoming appointments are logged for {full_name}." + disclaimer(lang)
            appt_list = []
            for a in appointments:
                if not isinstance(a, dict):
                    continue
                adate = _safe_get(a, "date", "N/A")
                adoc = _safe_get(a, "with", "N/A")
                appt_list.append(f"- {adate} with {adoc}")
            appt_str = "\n".join(appt_list) if appt_list else "-"
            if lang == "hi":
                return f"{full_name} के आगामी अपॉइंटमेंट:\n\n{appt_str}" + disclaimer(lang)
            return f"Upcoming appointments for {full_name}:\n\n{appt_str}" + disclaimer(lang)

        # --- Vitals ------------------------------------------------------
        if any(k in msg for k in ["vitals", "bp", "blood pressure", "vaitals", "dhadkan"]):
            if not vitals:
                if lang == "hi":
                    return f"{full_name} के लिए कोई हालिया वाइटल्स दर्ज नहीं हैं।" + disclaimer(lang)
                return f"No recent vitals are logged for {full_name}." + disclaimer(lang)
            v_str = ", ".join(f"{k}: {v}" for k, v in vitals.items())
            if lang == "hi":
                return f"{full_name} के हालिया वाइटल्स: {v_str}" + disclaimer(lang)
            return f"Recent vitals for {full_name}: {v_str}" + disclaimer(lang)

        # --- Fallback -----------------------------------------------------
        if lang == "hi":
            return (
                f"मैंने {full_name} से संबंधित आपके प्रश्न का विश्लेषण किया है। "
                f"उनकी चार्ट स्थिति वर्तमान में '{status}' है। मैं कीमोथेरेपी शेड्यूल की जानकारी दे सकता हूँ या "
                "पैथोलॉजी रिपोर्ट का सारांश बना सकता हूँ, लेकिन मैं रियल-टाइम खुराक गणना या डायग्नोस्टिक स्टेजिंग "
                "घोषणाएँ नहीं कर सकता।"
            ) + disclaimer(lang)
        return (
            f"I've analyzed your question regarding {full_name}'s history. "
            f"Their current chart status is '{status}'. I can assist in mapping scheduled chemotherapy cycles or "
            "summarizing pathology reports, but I cannot make real-time dosage calculations or diagnostic staging "
            "declarations."
        ) + disclaimer(lang)

    except Exception as exc:
        # Bilingual apology whenever something goes wrong, regardless of
        # where in the flow the failure happened.
        safe_lang = lang if lang in ("en", "hi") else "en"
        return apology(safe_lang) + f"(detail / विवरण: {exc})" + disclaimer(safe_lang)


