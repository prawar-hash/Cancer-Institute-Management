"""
Oncology Clinical Assistant (Simulated)
----------------------------------------
Advanced version with:
  - Bilingual support (Hindi + English), auto-detected per message
  - Richer patient-context handling (diagnoses, treatments, allergies,
    upcoming appointments, vitals)
  - Defensive error handling with polite apology messages in both languages
    whenever expected data is missing or malformed

NOTE: All "extraction" and "chat" functions below are simulations for
demo/testing purposes. They do not perform real OCR or real clinical
reasoning, and none of this should be used for actual patient care.
"""

import re

CLINICAL_DISCLAIMER_EN = (
    "\n\n[Warning: This content is generated by AI. It is not medical advice. "
    "A licensed clinician must verify all recommendations and staging "
    "classifications before making clinical decisions.]"
)

CLINICAL_DISCLAIMER_HI = (
    "\n\n[चेतावनी: यह जानकारी AI द्वारा तैयार की गई है। यह चिकित्सीय सलाह नहीं है। "
    "किसी भी क्लिनिकल निर्णय से पहले एक लाइसेंस-प्राप्त चिकित्सक द्वारा सभी सिफारिशों और "
    "स्टेजिंग वर्गीकरण की पुष्टि आवश्यक है।]"
)

APOLOGY_EN = (
    "I'm sorry — I couldn't find that information in the chart, or something "
    "went wrong while processing it. "
)
APOLOGY_HI = (
    "क्षमा करें — मुझे चार्ट में यह जानकारी नहीं मिली, या इसे प्रोसेस करते समय कुछ गड़बड़ी हुई। "
)


# ---------------------------------------------------------------------------
# Language detection
# ---------------------------------------------------------------------------

_DEVANAGARI_RE = re.compile(r"[\u0900-\u097F]")

_HINGLISH_HINTS = {
    "kya", "hai", "hain", "kaise", "kab", "kaun", "kripya", "batao", "bataiye",
    "mujhe", "aap", "aapka", "iska", "uska", "dawai", "dawaai", "bimari",
    "ilaj", "report", "marij", "rogi",
}


def detect_language(message: str) -> str:
    """
    Returns 'hi' if the message looks like Hindi (Devanagari script or
    common Hinglish/romanized-Hindi words), otherwise 'en'.
    """
    if not message:
        return "en"
    if _DEVANAGARI_RE.search(message):
        return "hi"
    tokens = re.findall(r"[a-zA-Z]+", message.lower())
    hits = sum(1 for tok in tokens if tok in _HINGLISH_HINTS)
    # If a meaningful fraction of the words look like romanized Hindi, treat as Hindi
    if tokens and hits / len(tokens) >= 0.15:
        return "hi"
    return "en"


def disclaimer(lang: str) -> str:
    return CLINICAL_DISCLAIMER_HI if lang == "hi" else CLINICAL_DISCLAIMER_EN


def apology(lang: str) -> str:
    return APOLOGY_HI if lang == "hi" else APOLOGY_EN


